package com.femsq.web.audit;

/**
 * Сервис-оркестратор выполнения ревизии (аналог btnAuditRun_Click в VBA).
 *
 * На этом этапе реализуется только каркас: метод будет вызываться асинхронно
 * из REST-контроллера и делегировать обработку файлов специализированным сервисам.
 */
public interface AuditExecutionService {

    /**
     * Выполняет ревизию с указанным идентификатором в фоновом режиме.
     * Фактическая реализация будет добавлена на следующих этапах.
     *
     * @param auditId идентификатор ревизии (ra_a.adt_key)
     */
    void executeAudit(long auditId);
}
